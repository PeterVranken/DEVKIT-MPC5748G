function importCanIfIntoTL()

% Import the generated XML specification of the CAN interface (file crt_canRT.xml) into the
% dSPACE TargetLink Data Dictionary.

    % Note, the merge strategy is required because the XML uses variable group "Variables"
    % as outermost folder. In real projects will Variables always contain a lot of entries
    % besides our CAN interface specification. Without the merge our import would replace
    % the complete contents of Variables with our imported data and hence discard all other
    % stuff.
    dsdd( 'Import' ...
        , 'Format', 'XML' ...
        , 'File', 'crt_canRT.xml' ...
        , 'parent', '//DD0/Pool' ...
        , 'ImportStrategy', 'merge' ...
        , 'Mode', 'extended' ...
        )
end